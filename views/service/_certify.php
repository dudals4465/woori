③ 회원이 자신의 ID 및 비밀번호를 도난당하거나 제3자가 사용하고 있음을 인지한 경우에는 바로 “서비스”를 통해 회사에 통보하고 “서비스” 및 회사의 안내가 있는 경우에는 그에 따라야 합니다.
④ 회원은 이 약관 및 관계법령에서 규정한 사항을 준수하여야 합니다.

제14조(이용자의 의무)
이용자는 다음 행위를 하여서는 안됩니다.
1. 신청 또는 변경시 허위 내용의 등록
