① “서비스“에서 작성된 저작물에 대한 저작권 기타 지적재산권은 회사에 귀속합니다.
② 이용자는 “서비스”를 이용함으로써 얻은 정보를 회사의 사전 승낙없이 복제, 송신, 출판, 배포, 방송 기타 방법에 의하여 영리목적으로 이용하거나 제3자에게 이용하게 하여서는 안됩니다.

제18조(분쟁해결)
① 회사는 이용자가 제기하는 정당한 의견이나 불만을 반영하고 그 피해를 보상처리하기 위하여 소비자 상담 화면을 설치·운영합니다.
② 회사는 이용자로부터 제출되는 불만사항 및 의견은 우선적으로 그 사항을 처리합니다. 다만, 신속한 처리가 곤란한 경우에는 이용자에게 그 사유와 처리일정을 통보해 드립니다.

제19조(재판권 및 준거법)
① 회사와 이용자간에 발생한 분쟁에 관한 소송은 회사 본점 소재지를 관할하는 법원을 전속 관할 법원으로 합니다.
