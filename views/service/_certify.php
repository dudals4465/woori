2. 다른 사람의 “서비스” 이용을 방해하거나 그 정보를 도용하는 등 전자거래질서를 위협하는 경우
3. “서비스”를 이용하여 법령과 이 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우
4. 기타 “서비스”가 정한 이용 신청요건을 미충족하였을 경우
③ 회사가 회원 자격을 제한·정지 시킨 후, 동일한 행위가 2회 이상 반복되거나 30일 이내에 그 사유가 시정되지 아니하는 경우 회사는 회원자격을 상실시킬 수 있습니다.
④ 회사가 회원자격을 상실시키는 경우에는 회원등록을 말소합니다.

제9조(회원에 대한 통지)
① 회사는 회원에 대한 통지를 하는 경우, 회원이 “서비스”에 제출한 전자우편주소, 유/무선 전화(SMS 포함) 등으로 할 수 있습니다.
② 회사는 불특정다수 회원에 대한 통지의 경우 1주일 이상 “서비스” 공지사항에 게시함으로서 개별 통지에 갈음할 수 있습니다.

제10조(금융상품신청)
“서비스” 이용자는  웹 서비스상에서 이하의 방법에 의하여 금융상품을 신청합니다.
