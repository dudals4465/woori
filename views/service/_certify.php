2. 회원의 전산조작 오류 등으로 인한 경우
3. 천재지변 등의 기타 불가피한 사유로 인한 경우
② 회사는 회원이 서비스를 이용하여 기대하는 수익을 상실한 것에 대하여 책임지지 않으며 그 밖에 서비스를 통해 얻은 자료로 인한 손해 등에 대하여도 책임지지 않습니다.

