3. “서비스”에 게시된 정보의 변경
4. “서비스”에서 정한 정보 이외의 정보(컴퓨터 프로그램 등)의 송신 또는 게시
5. “서비스” 기타 제3자의 저작권 등 지적재산권에 대한 침해
6. “서비스” 기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위
7. 외설 또는 폭력적인 메시지, 화상, 음성, 기타 공서양속에 반하는 정보를 서비스에 공개 또는 게시하는 행위

